public class OneSideTrianglePattern {

    public static void main(String[] args) {
// y9vu9rc09
        // y28lj2c2u
        int n=7;
        for(int i=0;i<n;i++){

            for(int j=n-i;j>=1;j--){

                System.out.print(" ");
            }

            for(int j=0;j<i;j++){

                System.out.print(" *");
            }

            System.out.println();
        }



    }

}
